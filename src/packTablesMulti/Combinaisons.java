package packTablesMulti;

public class Combinaisons {

	private int nbr1;
	private int nbr2;
	
	public Combinaisons() {
		super();
	}
	
	public Combinaisons(int nbr1, int nbr2) {
		this.nbr1 = nbr1;
		this.nbr2 = nbr2;
	}

	public int getNbr1() {
		return this.nbr1;
	}

	public void setNbr1(int nbr1) {
		this.nbr1 = nbr1;
	}

	public int getNbr2() {
		return this.nbr2;
	}

	public void setNbr2(int nbr2) {
		this.nbr2 = nbr2;
	}
}
